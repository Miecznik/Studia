public class Dog {

    String name;
    String breed;


    Dog( String nameInp, String breedImp ){

        this.name = nameInp;
        this.breed = breedImp;

    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

    public void setName(String name) {
        this.name = name;
    }
}
